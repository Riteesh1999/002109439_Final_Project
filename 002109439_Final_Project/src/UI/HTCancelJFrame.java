    private void GetHotels()
    {
        try{
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Airlines" , "root" , "");
            st = cn.createStatement();
            String Query = "select * from tblHTBookings";
            rs = st.executeQuery(Query);
            while(rs.next())
            {
                String t = rs.getString("BookingID");
                BookingID.addItem(t );
                
            }
        }catch(Exception e){
            
        }
        
    }
    
    
    private void GetBookingID()
    {
        String Query = "select * from tblHTBookings where BookingID ="+BookingID.getSelectedItem().toString();
        Statement st;
        ResultSet rs;
        try{
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Airlines" , "root" , "");
            st = cn.createStatement();
            rs = st.executeQuery(Query);
            if(rs.next()){
                HotelName.setText(rs.getString("HTName"));
  
            }
        }catch(Exception e){
            
        }
    }
    private void DisplayCancellations()
    {
        try{
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Airlines" , "root" , "");
            st = cn.createStatement();
            rs = st.executeQuery("Select * from HTCancel");
            TblCancellation.setModel(DbUtils.resultSetToTableModel(rs));
        }catch (Exception e){
            
        }
    }
    int CancelId = 0;
    
    private void CountCancelled()
    {
        try{
            st1 = cn.createStatement();
            rs1 = st1.executeQuery("Select Max(Cancid) from HTCancel");
            rs1.next();
            CancelId = rs1.getInt(1)+1;
            
        }catch (Exception e){
            
        }
    }
