    private void btnCancelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCancelMouseClicked
        if(HotelName.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this , "Missing Information");

        } else {
            try {
                CountCancelled ();

                cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Airlines" , "root" , "");
                PreparedStatement Add = cn.prepareStatement("insert into HTCancel values(?,?,?,?)");
                Add.setInt(1, CancelId);
                Add.setString(2, BookingID.getSelectedItem().toString());
                Add.setString(3, HotelName.getText());
                Add.setString(4, CnDate.getDate().toString());

                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Booking Cancelled Successfully");
                cn.close();
                DisplayCancellations();
                Cancel();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e);
            }
        }

    }//GEN-LAST:event_btnCancelMouseClicked

